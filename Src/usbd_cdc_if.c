//==============================================================================
// Ring buffer initialization
//==============================================================================
void USB_init_ring_buf(void){
	memset(ring_buf1,0,sizeof(ring_buf1));
	wp = 0;
	rp = 0;

	memset(PacketLen_ring_buf,0,sizeof(PacketLen_ring_buf));
	pl_wp =0;
	pl_rp =0;
}

//==============================================================================
// Get the number of characters received
//==============================================================================
u16 USB_get_cap(void){
	if(wp >= rp){
		return (wp - rp);
	}
	return (u16)(((s32)65535 + 1) + ((s32)wp - (s32)rp));
}

//==============================================================================
// Get 1 character from ring buffer
//==============================================================================
char USB_get_ring_buf(void){
	return ring_buf1[rp++ % RING_BUFFER_SIZE];
}

//==============================================================================
// Store 1 character in ring buffer
//==============================================================================
void USB_put_ring_buf(char c){
	ring_buf1[wp++ % RING_BUFFER_SIZE] = c;
}

//==============================================================================
// Get the number of received packets.
//==============================================================================
u16 USB_get_pl_cap(void){
	if(pl_wp >= pl_rp){
		return (pl_wp - pl_rp);
	}
	return (u16)(((s32)65535 + 1) + ((s32)pl_wp - (s32)pl_rp));
}

//==============================================================================
// Get 1 character from ring buffer for packet character length.
//==============================================================================
char USB_get_pl_ring_buf(void){
	return PacketLen_ring_buf[pl_rp++ % RING_BUFFER_SIZE];
}

//==============================================================================
// Store 1 character in ring buffer for packet character length.
//==============================================================================
void USB_put_pl_ring_buf(char c){
	PacketLen_ring_buf[pl_wp++ % RING_BUFFER_SIZE] = c;
}
